// API functions for Indian locations
export const getIndianStates = () => {
  return Object.keys(indianStates).map(state => ({
    name: state,
    code: indianStates[state].code,
    capital: indianStates[state].capital,
    coordinates: indianStates[state].coordinates
  }));
};

export const getCitiesByState = (stateName) => {
  const state = indianStates[stateName];
  if (!state) return [];
  
  return state.majorCities.map(city => ({
    name: city,
    state: stateName,
    coordinates: state.coordinates,
    code: state.code
  }));
};

export const getPopularDestinations = () => {
  return Object.keys(popularDestinations).map(destination => ({
    name: destination,
    state: popularDestinations[destination].state,
    coordinates: popularDestinations[destination].coordinates,
    description: popularDestinations[destination].description,
    bestSeason: popularDestinations[destination].bestSeason
  }));
};

export const getDestinationDetails = (destinationName) => {
  return popularDestinations[destinationName] || null;
};

export const searchDestinations = (query) => {
  const results = [];
  const searchTerm = query.toLowerCase();
  
  // Search in popular destinations
  Object.keys(popularDestinations).forEach(dest => {
    if (dest.toLowerCase().includes(searchTerm)) {
      results.push({
        name: dest,
        type: 'destination',
        state: popularDestinations[dest].state,
        coordinates: popularDestinations[dest].coordinates
      });
    }
  });
  
  // Search in states
  Object.keys(indianStates).forEach(state => {
    if (state.toLowerCase().includes(searchTerm)) {
      results.push({
        name: state,
        type: 'state',
        code: indianStates[state].code,
        coordinates: indianStates[state].coordinates
      });
    }
    
    // Search in cities of this state
    indianStates[state].majorCities.forEach(city => {
      if (city.toLowerCase().includes(searchTerm)) {
        results.push({
          name: city,
          type: 'city',
          state: state,
          coordinates: indianStates[state].coordinates
        });
      }
    });
  });
  
  return results.slice(0, 10); // Return top 10 results
};

export const getNearbyDestinations = (coordinates, radius = 200) => {
  const results = [];
  const [lat, lng] = coordinates;
  
  // Calculate distance and find nearby destinations
  Object.keys(popularDestinations).forEach(dest => {
    const destCoords = popularDestinations[dest].coordinates;
    const distance = calculateDistance([lat, lng], destCoords);
    
    if (distance <= radius) {
      results.push({
        name: dest,
        state: popularDestinations[dest].state,
        coordinates: destCoords,
        distance: Math.round(distance)
      });
    }
  });
  
  return results.sort((a, b) => a.distance - b.distance);
};

// Helper function to calculate distance between two coordinates
const calculateDistance = (coord1, coord2) => {
  const R = 6371; // Earth's radius in km
  const dLat = toRad(coord2[0] - coord1[0]);
  const dLon = toRad(coord2[1] - coord1[1]);
  const lat1 = toRad(coord1[0]);
  const lat2 = toRad(coord2[0]);

  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.sin(dLon/2) * Math.sin(dLon/2) * Math.cos(lat1) * Math.cos(lat2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
};

const toRad = (value) => value * Math.PI / 180;

